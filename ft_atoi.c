int	ft_atoi(const char *str)
{
	int				i;
	int				m;
	unsigned long	n;

	i = 0;
	m = 1;
	n = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n' || str[i] == '\v'
		|| str[i] == '\f' || str[i] == '\r')
		i++;
	if (str[i] == '-')
		m = -1;
	if (str[i] == '-' || str[i] == '+')
	i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		n = (n * 10 + (str[i] - 48));
		i++;
	}
	if (n > 9223372036854775807 && m == -1)
		return (0);
	if (n >= 9223372036854775807 && m == 1)
		return (-1);
	return (n * m);
}
